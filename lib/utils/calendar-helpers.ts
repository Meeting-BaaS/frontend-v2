import { isSameDay } from "date-fns";
import type { CalendarEvent } from "@/types/calendars.types";

/**
 * Get CSS classes for event colors
 * Uses brand colors from globals.css for meeting platforms
 * Determines color based on meeting platform, bot scheduled status, and event status
 */
export function getEventColorClasses(event: CalendarEvent): string {
  // Platform-based colors
  switch (event.meeting_platform) {
    case "zoom":
      // Zoom - using zoom-blue
      return "bg-zoom-blue/20 hover:bg-zoom-blue/30 text-zoom-blue dark:bg-zoom-blue/15 dark:hover:bg-zoom-blue/25 dark:text-zoom-blue/90";
    case "meet":
      // Google Meet - using google-meet-green
      return "bg-google-meet-green/20 hover:bg-google-meet-green/30 text-google-meet-green dark:bg-google-meet-green/15 dark:hover:bg-google-meet-green/25 dark:text-google-meet-green/90";
    case "teams":
      // Teams - using teams-purple
      return "bg-teams-purple/20 hover:bg-teams-purple/30 text-teams-purple dark:bg-teams-purple/15 dark:hover:bg-teams-purple/25 dark:text-teams-purple/90";
    default:
      // Default to Neutral - Don't have a platform, can't attach a color to it
      return "bg-neutral-400/20 hover:bg-neutral-400/30 text-neutral-400 dark:bg-neutral-400/15 dark:hover:bg-neutral-400/25 dark:text-neutral-400/90";
  }
}

/**
 * Get CSS classes for border radius based on event position in multi-day events
 */
export function getBorderRadiusClasses(
  isFirstDay: boolean,
  isLastDay: boolean,
): string {
  if (isFirstDay && isLastDay) {
    return "rounded"; // Both ends rounded
  } else if (isFirstDay) {
    return "rounded-l rounded-r-none not-in-data-[slot=popover-content]:w-[calc(100%+5px)]"; // Only left end rounded
  } else if (isLastDay) {
    return "rounded-r rounded-l-none not-in-data-[slot=popover-content]:w-[calc(100%+4px)] not-in-data-[slot=popover-content]:-translate-x-[4px]"; // Only right end rounded
  } else {
    return "rounded-none not-in-data-[slot=popover-content]:w-[calc(100%+9px)] not-in-data-[slot=popover-content]:-translate-x-[4px]"; // No rounded corners
  }
}

/**
 * Check if an event is a multi-day event
 */
export function isMultiDayEvent(event: CalendarEvent): boolean {
  const eventStart = new Date(event.start);
  const eventEnd = new Date(event.end);
  return event.allDay || eventStart.getDate() !== eventEnd.getDate();
}

/**
 * Filter events for a specific day
 */
export function getEventsForDay(
  events: CalendarEvent[],
  day: Date,
): CalendarEvent[] {
  return events
    .filter((event) => {
      const eventStart = new Date(event.start);
      return isSameDay(day, eventStart);
    })
    .sort((a, b) => new Date(a.start).getTime() - new Date(b.start).getTime());
}

/**
 * Sort events with multi-day events first, then by start time
 */
export function sortEvents(events: CalendarEvent[]): CalendarEvent[] {
  return [...events].sort((a, b) => {
    const aIsMultiDay = isMultiDayEvent(a);
    const bIsMultiDay = isMultiDayEvent(b);

    if (aIsMultiDay && !bIsMultiDay) return -1;
    if (!aIsMultiDay && bIsMultiDay) return 1;

    return new Date(a.start).getTime() - new Date(b.start).getTime();
  });
}

/**
 * Get multi-day events that span across a specific day (but don't start on that day)
 */
export function getSpanningEventsForDay(
  events: CalendarEvent[],
  day: Date,
): CalendarEvent[] {
  return events.filter((event) => {
    if (!isMultiDayEvent(event)) return false;

    const eventStart = new Date(event.start);
    const eventEnd = new Date(event.end);

    // Only include if it's not the start day but is either the end day or a middle day
    return (
      !isSameDay(day, eventStart) &&
      (isSameDay(day, eventEnd) || (day > eventStart && day < eventEnd))
    );
  });
}

/**
 * Get all events visible on a specific day (starting, ending, or spanning)
 */
export function getAllEventsForDay(
  events: CalendarEvent[],
  day: Date,
): CalendarEvent[] {
  return events.filter((event) => {
    const eventStart = new Date(event.start);
    const eventEnd = new Date(event.end);
    return (
      isSameDay(day, eventStart) ||
      isSameDay(day, eventEnd) ||
      (day > eventStart && day < eventEnd)
    );
  });
}

/**
 * Add hours to a date
 */
export function addHoursToDate(date: Date, hours: number): Date {
  const result = new Date(date);
  result.setHours(result.getHours() + hours);
  return result;
}
